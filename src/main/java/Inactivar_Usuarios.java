    // Método para inactivar
    private void inactivarUsuario(String id) {

        for (int i = 0; i < Usuarios.usuarios.length; i++) {

            if (Usuarios.usuarios[i][0].equals(id)) {
                Usuarios.usuarios[i][4] = "INACTIVO";
                return;
            }
        }
    }

