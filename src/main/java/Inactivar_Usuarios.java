    // Botón para inactivar usuarios
    private void inactivarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inactivarActionPerformed

        int fila = ver_usu.getSelectedRow();

        if (fila < 0) {
            JOptionPane.showMessageDialog(this, "Seleccione un usuario", "Información", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        String id = ver_usu.getValueAt(fila, 0).toString();
        String rol = ver_usu.getValueAt(fila, 3).toString();
        String estado = ver_usu.getValueAt(fila, 4).toString();

        // --------------------------- LINEAMIENTOS PARA INACTIVAR UN USUARIO -------------------------
        // No se puede inactivar al encargado
        if (rol.equalsIgnoreCase("Encargado")) {
            JOptionPane.showMessageDialog(this, "No puedes inactivar al encargado",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // El usuario ya está inactivo
        if (estado.equalsIgnoreCase("INACTIVO")) {
            JOptionPane.showMessageDialog(this, "El usuario ya está inactivo",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "¿Está seguro que desea inactivar este usuario?", "Confirmar", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            inactivarUsuario(id);
            JOptionPane.showMessageDialog(this, "Usuario inactivo", "Operación exitosa", JOptionPane.INFORMATION_MESSAGE);
            Usuarios.cargarTabla(ver_usu);
            inactivar.setVisible(false);
        }

    }//GEN-LAST:event_inactivarActionPerformed

