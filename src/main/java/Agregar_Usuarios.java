    // Botón agregar
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String usuario = Usu1.getText();
        String pass = Contra1.getText();
        String pass2 = Contra.getText();
        String rol = roles.getSelectedItem().toString();

        // -------------------------- VALIDACIONES -------------------------
        if (usuario.isEmpty() || pass.isEmpty() || pass2.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Complete todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!pass.equals(pass2)) {
            JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String error = Usuarios.validarPassword(pass);
        if (error != null) {
            JOptionPane.showMessageDialog(this, error, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Encargado
        if (rol.equals("Encargado")) {

            int confirmar = JOptionPane.showConfirmDialog(
                    this,
                    "Solo puede existir 1 encargado.\n¿Desea reemplazar al encargado actual?",
                    "Confirmar",
                    JOptionPane.YES_NO_OPTION
            );

            if (confirmar != JOptionPane.YES_OPTION) {
                return;
            }

            // Buscar encargado activo actual
            int indexEnc = -1;
            for (int i = 0; i < Usuarios.usuarios.length; i++) {
                if (Usuarios.usuarios[i][3].equals("Encargado")
                        && Usuarios.usuarios[i][4].equals("ACTIVO")) {
                    indexEnc = i;
                    break;
                }
            }

            // Si existe encargado activo → pedir credenciales
            if (indexEnc != -1) {
                String userEnc = JOptionPane.showInputDialog("Usuario encargado actual:");
                String passEnc = JOptionPane.showInputDialog("Contraseña del encargado actual:");

                if (!Usuarios.usuarios[indexEnc][1].equals(userEnc)
                        || !Usuarios.usuarios[indexEnc][2].equals(passEnc)) {

                    JOptionPane.showMessageDialog(this,
                            "Credenciales del encargado incorrectas\nNo se puede asignar nuevo encargado",
                            "Error",
                            JOptionPane.ERROR_MESSAGE
                    );
                    return;
                }

                // inactivar al usuario viejo
                Usuarios.usuarios[indexEnc][4] = "INACTIVO";

                // Si es el mismo que está logueado, debe cerrar sesión
                if (Usuarios.Sesion.usuarioActual.equals(userEnc)) {

                    JOptionPane.showMessageDialog(this, "Encargado reemplazado",
                            "Operación Exitosa", JOptionPane.INFORMATION_MESSAGE);
                    JOptionPane.showMessageDialog(this,
                            "Su usuario ha sido inactivado\nSerá redirigido al inicio de sesión",
                            "Sesión finalizada", JOptionPane.WARNING_MESSAGE);
                    this.dispose();
                    new Login().setVisible(true);
                }
            }

            // Crear nuevo encargado (No se borra el viejo)
            String[][] nuevoEnc = new String[Usuarios.usuarios.length + 1][5];

            for (int i = 0; i < Usuarios.usuarios.length; i++) {
                nuevoEnc[i] = Usuarios.usuarios[i];
            }

            nuevoEnc[nuevoEnc.length - 1] = new String[]{
                generarID(),
                usuario,
                pass,
                "Encargado",
                "ACTIVO"
            };

            Usuarios.usuarios = nuevoEnc;

            Usuarios.limpiarCampos(Usu1);
            Usuarios.limpiarPassword(Contra1, Contra);
            roles.setSelectedIndex(0);

            return;
        }

        // Supervisores
        if (rol.equals("Supervisor") && contarSupervisor() >= 3) {

            JOptionPane.showMessageDialog(this,
                    "Se alcanzó el límite de 3 supervisores\n"
                    + "Por favor modifique un usuario para agregar un nuevo supervisor",
                    "Límite alcanzado",
                    JOptionPane.WARNING_MESSAGE);

            Modif_Usuarios mod = new Modif_Usuarios();
            mod.setVisible(true);
            this.dispose();
            return;
        }

        // Empleados
        String[][] nuevo = new String[Usuarios.usuarios.length + 1][5];

        for (int i = 0; i < Usuarios.usuarios.length; i++) {
            nuevo[i] = Usuarios.usuarios[i];
        }

        nuevo[nuevo.length - 1] = new String[]{
            generarID(),
            usuario,
            pass,
            rol,
            "ACTIVO"
        };

        Usuarios.usuarios = nuevo;

        JOptionPane.showMessageDialog(this,
                "Usuario agregado correctamente",
                "Éxito",
                JOptionPane.INFORMATION_MESSAGE);

        Usuarios.limpiarCampos(Usu1);
        Usuarios.limpiarPassword(Contra1, Contra);
        roles.setSelectedIndex(0);


    }//GEN-LAST:event_jButton1ActionPerformed

