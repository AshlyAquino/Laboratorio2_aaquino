    // Contar supervisores
    private int contarSupervisor() {

        int contador = 0;

        for (int i = 0; i < Usuarios.usuarios.length; i++) {

            if (Usuarios.usuarios[i][3].equalsIgnoreCase("Supervisor") && Usuarios.usuarios[i][4].equalsIgnoreCase("ACTIVO")) {
                contador++;
            }
        }

        return contador;
    }

    // Ocultar opciones del comboBox
    private void actualizarComboRoles() {

        roles.removeAllItems();

        // Si ya hay 3 supervisores activos se bloquea
        if (contarSupervisor() >= 3) {
            roles.addItem("Encargado");
            roles.addItem("Empleado");
            roles.setSelectedItem("Empleado");
        } else {
            // Todas las opciones disponibles
            roles.addItem("Encargado");
            roles.addItem("Supervisor");
            roles.addItem("Empleado");
        }

        roles.setEnabled(true);

    }

    // Método para generar ID
    private String generarID() {

        return Usuarios.generarID();

    }

    // Validar permisos de acceso
    private void validarPermisos() {

        String rol = Usuarios.Sesion.rolActual;

        if (!rol.equals("Encargado")) {
            JOptionPane.showMessageDialog(this, "No tienes permiso de agregar nuevos usuarios.\n" + "Solo el encargado general puede agregar nuevos usuarios",
                    "Error", JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }

    }

