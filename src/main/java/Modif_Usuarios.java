    // Método contar supervisores
    private int contar_supervisor() {

        int contador = 0;

        for (int i = 0; i < Usuarios.usuarios.length; i++) {
            if (Usuarios.usuarios[i][3].equalsIgnoreCase("Supervisor") && Usuarios.usuarios[i][4].equalsIgnoreCase("ACTIVO")) {
                contador++; // Solo se permiten 3 supervisores activos registrados
            }
        }
        return contador;
    }

