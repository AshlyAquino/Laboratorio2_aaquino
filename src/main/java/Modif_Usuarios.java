    // Botón Modificar Usuarios
    private void modiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modiActionPerformed

        int fila = ver_usu.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(this, "Seleccione un usuario", "Información", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        String id = ver_usu.getValueAt(fila, 0).toString();
        String rolActual = ver_usu.getValueAt(fila, 3).toString();

        // -------------------------- PEDIR NUEVOS DATOS -------------------------
        // Pedir nuevo usuario 
        String nuevoUsuario = JOptionPane.showInputDialog(this, "Nuevo usuario:");
        if (nuevoUsuario == null || nuevoUsuario.trim().isEmpty()) {
            return;
        }

        // Pedir y validad nueva contraseña
        String nuevaPass = JOptionPane.showInputDialog(this, "Nueva contraseña:");
        if (nuevaPass == null || nuevaPass.trim().isEmpty()) {
            return;
        }
        String validarPass = JOptionPane.showInputDialog(this, "Validar contraseña:");
        if (!nuevaPass.equals(validarPass)) {
            JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String error = Usuarios.validarPassword(nuevaPass);
        if (error != null) {
            JOptionPane.showMessageDialog(this, error, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Pedir nuevo rol
        String nuevoRol;
        String[] opciones = {"Encargado", "Supervisor", "Empleado"};
        boolean rolValido = false;
        while (!rolValido) {

            nuevoRol = (String) JOptionPane.showInputDialog(this, "Seleccione nuevo rol:", "Modificar Rol",
                    JOptionPane.QUESTION_MESSAGE, null, opciones, rolActual);

            // Si cancela
            if (nuevoRol == null) {
                return;
            }

            // Validar rol de supervisor
            if (nuevoRol.equals("Supervisor") && contar_supervisor() >= 3) {
                JOptionPane.showMessageDialog(this, "Límite de Supervisores alcanzado\nSeleccione otro rol",
                        "Límite alcanzado", JOptionPane.WARNING_MESSAGE);
                continue; // Vuelve a pedir rol
            }

            // Validar rol para cambiar de encargado
            if (nuevoRol.equals("Encargado")) {
                int confirmar = JOptionPane.showConfirmDialog(
                        this,
                        "¿Desea asignar un nuevo encargado?\nSolo puede existir 1 encargado activo.",
                        "Confirmar cambio",
                        JOptionPane.YES_NO_OPTION
                );
                if (confirmar != JOptionPane.YES_OPTION) {
                    return;
                }
                int indexEncargado = -1;
                String userEnc = "";
                String passEnc = "";

                for (int i = 0; i < Usuarios.usuarios.length; i++) {
                    if (Usuarios.usuarios[i][3].equals("Encargado")
                            && Usuarios.usuarios[i][4].equals("ACTIVO")) {
                        indexEncargado = i;
                        userEnc = Usuarios.usuarios[i][1];
                        passEnc = Usuarios.usuarios[i][2];
                        break;
                    }
                }

                String userInput = JOptionPane.showInputDialog("Usuario encargado actual:");
                String passInput = JOptionPane.showInputDialog("Contraseña:");

                if (userInput == null || passInput == null || !userInput.equals(userEnc) || !passInput.equals(passEnc)) {

                    JOptionPane.showMessageDialog(this, "Credenciales incorrectas", "Denegado", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                Usuarios.usuarios[indexEncargado][4] = "INACTIVO"; // Se inactiva el encargado anterior, ya que solo se permite tener 1 encargado cativo registrado

                for (int i = 0; i < Usuarios.usuarios.length; i++) {
                    if (Usuarios.usuarios[i][0].equals(id)) {
                        Usuarios.usuarios[i][1] = nuevoUsuario;
                        Usuarios.usuarios[i][2] = nuevaPass;
                        Usuarios.usuarios[i][3] = "Encargado";
                        Usuarios.usuarios[i][4] = "ACTIVO";
                        if (Usuarios.Sesion.usuarioActual.equals(userEnc)) {
                            JOptionPane.showMessageDialog(this, "Encargado modificado", "Operación Exitosa", JOptionPane.INFORMATION_MESSAGE);
                            JOptionPane.showMessageDialog(this,"Su usuario ha sido inactivado\n"
                                    + "Será redirigido al inicio de sesión", "Sesión finalizada", JOptionPane.WARNING_MESSAGE);
                            this.dispose();
                            new Login().setVisible(true);
                            return;
                        }
                    }
                }

                JOptionPane.showMessageDialog(this, "Encargado actualizado", "Operación Exitosa", JOptionPane.INFORMATION_MESSAGE);
                rolValido = true;

            } else {
                //Modificar los datos
                for (int i = 0; i < Usuarios.usuarios.length; i++) {

                    if (Usuarios.usuarios[i][0].equals(id)) {
                        Usuarios.usuarios[i][1] = nuevoUsuario;
                        Usuarios.usuarios[i][2] = nuevaPass;
                        Usuarios.usuarios[i][3] = nuevoRol;
                        break;
                    }
                }

                JOptionPane.showMessageDialog(this, "Usuario modificado", "Operación Exitosa", JOptionPane.INFORMATION_MESSAGE);
                rolValido = true;
            }
        }
        // Actualizar tabla
        Usuarios.cargarTabla(ver_usu);
        modi.setVisible(false);
    }//GEN-LAST:event_modiActionPerformed

